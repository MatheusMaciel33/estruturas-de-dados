
public class Carta {

	private String nome;
	private String imagem;

	public Carta() {
	}

	public Carta(String nome, String imagem) {
		super();
		this.nome = nome;
		this.imagem = imagem;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getImagem() {
		return imagem;
	}

	public void setImagem(String imagem) {
		this.imagem = imagem;
	}

}
